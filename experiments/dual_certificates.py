def solve3(M, b):
    a, bb, c = M
    det = (a[0]*(bb[1]*c[2]-bb[2]*c[1]) - a[1]*(bb[0]*c[2]-bb[2]*c[0])
           + a[2]*(bb[0]*c[1]-bb[1]*c[0]))
    if det == 0:
        return None
    x = []
    for k in range(3):
        Mk = [row[:] for row in M]
        for r in range(3):
            Mk[r][k] = b[r]
        a2, b2, c2 = Mk
        dk = (a2[0]*(b2[1]*c2[2]-b2[2]*c2[1]) - a2[1]*(b2[0]*c2[2]-b2[2]*c2[0])
              + a2[2]*(b2[0]*c2[1]-b2[1]*c2[0]))
        x.append(F(dk, 1)/det)
    return x


def verify(name, data):
    V = [tuple(F(x) for x in v) for v in data['V']]
    ks = [F(x) for x in data['knots']]
    ys = [[F(x) for x in row] for row in data['psi']]
    n = len(ks)
    # mass (trapezoids) and exact normalization
    mass = sum(sum((ks[r+1]-ks[r])*(row[r]+row[r+1])/2 for r in range(n-1))
               for row in ys)
    assert mass > 0
    ys = [[v/mass for v in row] for row in ys]
    def psi(i, t):
        if t <= ks[0]:
            return ys[i][0]
        if t >= ks[-1]:
            return ys[i][-1]
        r = bisect_right(ks, t) - 1
        if ks[r] == t:
            return ys[i][r]
        w = (t-ks[r])/(ks[r+1]-ks[r])
        return ys[i][r]*(1-w) + ys[i][r+1]*w
    def uval(v, lam):
        return v[0]*lam[0]+v[1]*lam[1]+v[2]*lam[2]
    # arrangement vertices
    C = [(F(1),F(0),F(0)), (F(0),F(1),F(0)), (F(0),F(0),F(1))]
    pts = set(C)
    def line(i, t):
        v = V[i]
        return ([v[0]-v[2], v[1]-v[2]], t - v[2])
    for i, j in combinations(range(3), 2):
        for ti in ks:
            Li = line(i, ti)
            for tj in ks:
                Lj = line(j, tj)
                M = [[Li[0][0], Li[0][1], F(0)], [Lj[0][0], Lj[0][1], F(0)],
                     [F(0), F(0), F(1)]]
                x = solve3(M, [Li[1], Lj[1], F(0)])
                if x is None:
                    continue
                l3 = 1 - x[0] - x[1]
                if x[0] >= 0 and x[1] >= 0 and l3 >= 0:
                    pts.add((x[0], x[1], l3))
    for i in range(3):
        v = V[i]
        for t in ks:
            for P, Q in ((C[0],C[1]), (C[1],C[2]), (C[2],C[0])):
                a, b = uval(v,P), uval(v,Q)
                if a == b:
                    continue
                s = (t-a)/(b-a)
                if 0 <= s <= 1:
                    pts.add(tuple(P[k]+s*(Q[k]-P[k]) for k in range(3)))
    B = min(sum(psi(i, uval(V[i], lam)) for i in range(3)) for lam in pts)
    tgt = F(data['target'])
    ok = B > tgt if data['strict'] else B >= tgt
    mom = F(data['moment'])
    rel = '>' if data['strict'] else '>='
    assert ok, (name, B, tgt)
    assert B > mom or (not data['strict'] and tgt > mom) or tgt == mom
    print(f"  {name}: certified D {rel} {tgt} (exact min {float(B):.7f}); "
          f"moment bound {mom} = {float(mom):.7f}  OK")


